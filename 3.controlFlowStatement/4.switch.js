//조건문 Conditional Statement
//switch
//if else if else if else if ...else
//정해진 범위 안의 값에 대해 특정한 일을 해야 하는 경우
let day = 6; //0:월요일, 1:화요일...6:일요일
let dayName;
if(day === 0){
  dayName = 'Mon';
} else if(day === 1){
  dayName = 'Tue';
} else if(day === 2){
  dayName = 'Wed';
}else if(day === 3){
  dayName = 'Thu';
} else if(day === 4){
  dayName = 'Fri';
} else if(day === 5){
  dayName = 'Sat';
} else if(day === 6){
  dayName = 'Sun';
}